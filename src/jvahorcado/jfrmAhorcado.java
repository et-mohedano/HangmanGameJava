    public void actualizarGuiones(){
        guionesPalabras = "";
        for (int i = 0; i < objAhorcado.getLetrasPlabra()[0].length; i++) {
            if (objAhorcado.getLetrasPlabra()[1][i] == '0') {
               guionesPalabras += objAhorcado.getLetrasPlabra()[0][i] + "   "; 
            }else{
              guionesPalabras += "_   ";
            }
        }
        jlblPalabraSelec.setText(guionesPalabras);
    }
    public void comprobarJuego(){
        boolean palabraCompleta = true;
        if (objAhorcado.getNumIntentos()==6) {
            JOptionPane.showMessageDialog(null, "¡Lo siento te has ahorcado, sigue intentandolo!");
            jbtnIngresarLetra.setEnabled(false);
        }else{
            for (int i = 0; i < objAhorcado.getLetrasPlabra()[0].length; i++) {
                if (objAhorcado.getLetrasPlabra()[1][i] == '1') {
                    palabraCompleta = false;
                }
            }
            if (palabraCompleta) {
                JOptionPane.showMessageDialog(null, "¡Felicidades adivinaste la palabra!");
                jbtnIngresarLetra.setEnabled(false);
            }
        }
    }
    public void actualizarImagen(){
        mostrarImagen(jlblAhorcado, imagenesAhorcado[objAhorcado.getNumIntentos()]);
       
    }
