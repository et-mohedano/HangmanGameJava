    private void jbtnGenerarPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnGenerarPActionPerformed
        // TODO add your handling code here
        //objAhorcado.setNumIntentos(0);
        limpiarControles();
        jbtnIngresarLetra.setEnabled(true);
        jbtnJugar.setEnabled(true);
        jbtnLimpiar.setEnabled(true);
        objAhorcado.generarNumPalabras();
        //jlblIntentosFallos.setText(String.valueOf(objAhorcado.getNumIntentos()));
        jtxtNumPalabras.setText(String.valueOf(objAhorcado.getNumPalabras()));
        arrPalabras = new String[2][objAhorcado.getNumPalabras()];
        // Preparar arreglo
        for (int i = 0; i < objAhorcado.getNumPalabras(); i++) {
            arrPalabras[0][i] = "";
            arrPalabras[1][i] = "0";
        }
        JOptionPane.showMessageDialog(null, "Ingresa " + objAhorcado.getNumPalabras() + " palabras:");
        for (int i = 0; i < objAhorcado.getNumPalabras(); i++) {
            while (repetirPalabra) {                
                valorLstP = JOptionPane.showInputDialog(null, "Ingresa la palabra " + (i+1) + ":");
                if (objAhorcado.validadPalabra(valorLstP, arrPalabras) == 0) {
                    arrPalabras[0][i] = objAhorcado.normalizarPalabra(valorLstP);
                    repetirPalabra = false;
                } else if (objAhorcado.validadPalabra(valorLstP, arrPalabras) == 1){
                    JOptionPane.showMessageDialog(null, "Ingresa una palabra de mímino 2 letras");
                } else if (objAhorcado.validadPalabra(valorLstP, arrPalabras) == 2){
                    JOptionPane.showMessageDialog(null, "Ingresa una palabra sin espacios");
                } else if (objAhorcado.validadPalabra(valorLstP, arrPalabras) == 3){
                    JOptionPane.showMessageDialog(null, "Ingresa una palabra que no hayas ingresado antes");
                } else if (objAhorcado.validadPalabra(valorLstP, arrPalabras) == 4){
                    JOptionPane.showMessageDialog(null, "Ingresa solo letras, no números o símbolos");
                } else if (objAhorcado.validadPalabra(valorLstP, arrPalabras) == 5){
                    JOptionPane.showMessageDialog(null, "Ingresa una palabra que no incluya la letra ñ");
                }
            }
            repetirPalabra = true;
        }
        lstPalabras.clear();
        for (int i = 0; i < arrPalabras[0].length; i++) {
            lstPalabras.addElement(arrPalabras[0][i]);
        }
        objAhorcado.setPalabras(arrPalabras);
        // Solo si se quiere jugar inmediatamente:
        // objAhorcado.seleccionarPalabra();
        // jlblIntentosFallos.setText(String.valueOf(objAhorcado.getNumIntentos()));
        // actualizarGuiones();
    }//GEN-LAST:event_jbtnGenerarPActionPerformed

    private void jbtnIngresarLetraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnIngresarLetraActionPerformed
        // TODO add your handling code here:
        String letraIngresada = "";
        int rValidar;
        repetirPalabra = true; // Será repetir letra
        while (repetirPalabra) {
            letraIngresada = JOptionPane.showInputDialog(null, "Ingresa una letra:");
            rValidar = objAhorcado.validarLetra(letraIngresada);
            if (rValidar == 0) {
                letraIngresada = objAhorcado.normalizarPalabra(letraIngresada);
                repetirPalabra = false;
                 if(!objAhorcado.compararLetra(letraIngresada)){
                JOptionPane.showMessageDialog(null, "Intento fallido, prueba con otra letra");
            }
            } else if (rValidar == 1) {
                JOptionPane.showMessageDialog(null, "No ingreses más de una letra o espacios vacios");
            } else if (rValidar == 2) {
                JOptionPane.showMessageDialog(null, "Ingresa una letra sin espacios");
            } else if (rValidar == 3) {
                JOptionPane.showMessageDialog(null, "Ingresa una letra que no hayas ingresado antes, esto se cuenta como 1 error");
            } else if (rValidar == 4) {
                JOptionPane.showMessageDialog(null, "Ingresa solo letras, no números o símbolos");
            } else if (rValidar == 5) {
                JOptionPane.showMessageDialog(null, "Ingresa una letra diferente a la ñ");
            }
            jlblIntentosFallos.setText(String.valueOf(objAhorcado.getNumIntentos()));
           
            jlblIntentosFallos.setText(String.valueOf(objAhorcado.getNumIntentos()));
            actualizarGuiones();
            comprobarJuego();
            actualizarImagen();
            if(objAhorcado.getNumIntentos() == 6){
                break;
            }
           
        }
        
    }//GEN-LAST:event_jbtnIngresarLetraActionPerformed

